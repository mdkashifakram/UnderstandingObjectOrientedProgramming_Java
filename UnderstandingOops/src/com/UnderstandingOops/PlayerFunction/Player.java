package com.UnderstandingOops.PlayerFunction;

public class Player {
	private String playername;
	private int score;
	
	public Player(String playername, int score) {
		this.playername = playername;
		this.score = score;
	}

	public String getPlayername() {
		return playername;
	}

	public int getScore() {
		return score;
	}
	
	

}
